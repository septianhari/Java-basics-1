package com.tutorial;

public class Main {

    public static void main(String[] args) {
        int i = 0; // Inisialisasi variabel i dengan nilai awal
        while (i < 5) { // Selama i kurang dari 5
            System.out.println("Nilai i adalah: " + i);
            i++; // Menambahkan 1 ke nilai i
        }
    }
}
